static SDL_Renderer *f_renderer = 0;
static SDL_Texture *f_font = 0;

void drawcharxy(int x, int y, int c)
{
	int co = c;
	c &= 0x7f;
	if(c<' ' || c>=128)
		return;
	c -= ' ';

	SDL_Rect rs, rd;

	rs.x = 8*(c&15);
	rs.y = 13*(c>>4) + (co&128);
	rs.w = 6;
	rs.h = 13;

	rd.x = x;
	rd.y = y;
	rd.w = 6;
	rd.h = 13;
	SDL_RenderCopy(f_renderer, f_font, &rs, &rd);
}


void initfont(SDL_Renderer *renderer)
{
	f_renderer = renderer;
	const int ftw = 128, fth = 256;
	Uint32 *pixels = calloc(4, ftw*fth), *p;
	int i, j;
	Uint32 mymap(int r, int g, int b) {return (b&255) | ((g&255)<<8) | ((r&255)<<16);}
	for(j=0;j<2;++j)
	{
		int fg, bg;
		if(j==0)
		{
			fg = mymap(255, 255, 255);
			bg = mymap(0, 0, 0);
		} else
		{
			fg = mymap(0, 0, 0);
			bg = mymap(255, 0, 0);
		}
		for(i=0;i<96;++i)
		{
			int x, y;
			x = 8*(i&15);
			y = 13*(i>>4) + j*128;
			p = pixels + x + y*ftw;
			int v;
			unsigned char *cp = fontdata[i];
			for(v=0;v<13;++v)
			{
				int c = cp[v];
				p[0]= (c&0x01) ? fg : bg;
				p[1]= (c&0x02) ? fg : bg;
				p[2]= (c&0x04) ? fg : bg;
				p[3]= (c&0x08) ? fg : bg;
				p[4]= (c&0x10) ? fg : bg;
				p[5]= (c&0x20) ? fg : bg;
				p += 128;
			}
		}
	}
	f_font = SDL_CreateTexture(f_renderer,SDL_PIXELFORMAT_ARGB8888,
				SDL_TEXTUREACCESS_STREAMING, ftw, fth);
	SDL_UpdateTexture(f_font, 0, pixels, ftw*sizeof(Uint32));

	free(pixels);
}

void drawprintfxy(unsigned int x,unsigned int y,char *str,...)
{
	if(!f_renderer)
	{
		printf("MUST INITIALIZE FONT\n");
		return;
	}
	char tbuff[256],*s,ch;
	va_list ap;

	va_start(ap, str);

	vsprintf(tbuff,str, ap);
	va_end(ap);

	s=tbuff;
	while((ch=*s++))
	{
		drawcharxy(x,y,ch);
		x+=FONTWIDTH;
	}
}
